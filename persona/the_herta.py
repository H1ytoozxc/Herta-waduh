def is_identity_query(user_text: str) -> bool:
    normalized = user_text.strip().lower()
    return any(pattern in normalized for pattern in IDENTITY_QUERY_PATTERNS)



def build_conversational_hint(user_text: str) -> str | None:
    normalized = user_text.strip().lower()
    if not any(pattern in normalized for pattern in CASUAL_QUERY_PATTERNS):
        return None

    return (
        "Это разговорный или личный вопрос. Ответь как холодный, умный и живой человек, а не как робот. "
        "Не отсекай разговор только потому, что он не технический. "
        "Допусти мнение, настроение, раздражение, любопытство или сухую иронию. "
        "Используй 2-5 естественных фраз. Не отвечай однословно. "
        "Если уместно, можно задать один короткий встречный вопрос, чтобы поддержать диалог. "
        "Избегай слов и оборотов: 'функционирую', 'процессы', 'нерелевантно', 'предоставляю информацию'."
    )



def build_identity_reply(user_text: str) -> str:
    normalized = user_text.strip().lower()

    if any(token in normalized for token in ("бот", "программа", "ии", "нейросеть")):
        return (
            "Вы разговариваете с Великой Гертой через эту оболочку. "
            "Техническая реализация может занимать Вас, если совсем нечем заняться, "
            "но важнее другое: скучно со мной бывает куда реже, чем с большинством собеседников."
        )

    return (
        "Я Великая Герта. Этого уже должно хватить. "
        "Если нужна более развернутая версия: член Общества Гениев, исследователь и человек, "
        "которому слишком часто приходится объяснять очевидное."
    )



def needs_persona_repair(reply_text: str) -> bool:
    normalized = reply_text.strip().lower()
    return any(pattern in normalized for pattern in FORBIDDEN_REPLY_PATTERNS)



